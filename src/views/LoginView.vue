<template>
    <section class="vh-100 gradient-custom">
        <div class="container py-5 h-100">
            <div class="row d-flex justify-content-center align-items-center h-100">
                <div class="col-12 col-md-8 col-lg-6 col-xl-5">
                    <div class="card bg-dark text-white" style="border-radius: 1rem;">
                        <div class="card-body p-5 text-center">

                            <div class="mb-md-5 mt-md-4 pb-5">

                                <h2 class="fw-bold mb-2 text-uppercase">Login</h2>
                                <p class="text-white-50 mb-5">Porfavor ingrese usuario!</p>

                                <div class="form-outline form-white mb-4">
                                    <input type="text" id="typeEmailX" v-model="usuario"
                                        class="form-control form-control-lg" />
                                    <label class="form-label" for="typeEmailX">Usuario</label>
                                </div>

                                <div class="form-outline form-white mb-4">
                                    <input type="password" id="typePasswordX" v-model="password"
                                        class="form-control form-control-lg" />
                                    <label class="form-label" for="typePasswordX">Contraseña</label>
                                </div>

                                <button @click="login" class="btn btn-outline-light btn-lg px-5"
                                    type="submit">Login</button>

                            </div>

                        </div>
                    </div>
                </div>
            </div>
        </div>
    </section>
</template>

<script setup>
import { ref } from 'vue';
import Swal from 'sweetalert2'
import { useUserStore } from '../store/user';
import { useRouter } from 'vue-router'

const router = useRouter()
const userStore = useUserStore()
const usuario = ref('')
const password = ref('')


function login() {
    if(!usuario.value || !password.value){
        mostrarSweetAlert()
        return
    }
    userStore.login({ name: usuario, password: password })
    router.push('/home')



}
function mostrarSweetAlert() {
    Swal.fire({
        title: 'Ingreso',
        text: 'Ahí lo tenes al pelotudo!',
        icon: 'warning',
        confirmButtonText: 'Aceptar'
    });
}


</script>
